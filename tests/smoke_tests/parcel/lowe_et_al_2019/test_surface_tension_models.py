    @staticmethod
    def test_bulk_surface_tension_is_sgm_w():
        # arrange
        formulae = Formulae(surface_tension="Constant", constants=LOWE_CONSTS)

        # act
        sigma = formulae.surface_tension.sigma(np.nan, V_WET, np.nan, np.nan)

        # assert
        assert sigma == const.sgm_w

